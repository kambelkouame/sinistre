function getSteps() {
  return ['phase 1: Verification', 'Phase 2: Traitement', 'Phase 3:Verdict'];
}

function getStepContent(step) {
  switch (step) {
    case 0:
      return '1';
    case 1:
      return '2';
    case 2:
      return '3';
    default:
      return '4';
  }
}

export default function About() {
  const classes = useStyles();
  const [activeStep, setActiveStep] = React.useState(1);
  const steps = getSteps();

  const handleNext = () => {
    setActiveStep(prevActiveStep => prevActiveStep + 1);
  };

  const handleBack = () => {
    setActiveStep(prevActiveStep => prevActiveStep - 1);
  };

  const handleReset = () => {
    setActiveStep(0);
  };

  const [value, setValue] = React.useState(0);

  const handleChange = (event, newValue) => {
    setValue(newValue);
  };



 const [completed, setCompleted] = React.useState(0);
  const [buffer, setBuffer] = React.useState(10);

  const progress = React.useRef(() => {});
  React.useEffect(() => {
    progress.current = () => {
      if (completed > 100) {
        setCompleted(0);
        setBuffer(10);
      } else {
        const diff = Math.random() * 10;
        const diff2 = Math.random() * 10;
        setCompleted(completed + diff);
        setBuffer(completed + diff + diff2);
      }
    };
  });

  React.useEffect(() => {
    function tick() {
      progress.current();
    }
    const timer = setInterval(tick, 500);

    return () => {
      clearInterval(timer);
    };
  }, []);


  return (



  <Container>
     <Paper>

    <div className={classes.root}>

      <Stepper alternativeLabel activeStep={activeStep} connector={<ColorlibConnector />}>
        {steps.map(label => (
          <Step key={label}>
            <StepLabel StepIconComponent={ColorlibStepIcon}>{label}</StepLabel>
          </Step>
        ))}
      </Stepper>
		    

		    <Grid
			  container
			  direction="row"
			  justify="center"
			  alignItems="center"
			>

      <div>

           <CircularProgress />
			<CircularProgress color="secondary" />
    </div>
    </Grid>

      <div className={classes.root}>
      <LinearProgress variant="buffer" value={completed} valueBuffer={buffer} />
      <LinearProgress variant="buffer" value={completed} valueBuffer={buffer} color="secondary" />
    </div>



      <div>
        {activeStep === steps.length ? (
          <div>
            <Typography className={classes.instructions}>
            
            </Typography>
            <Button onClick={handleReset} className={classes.button}>
              Reset
            </Button>
          </div>
        ) : (
          <div>
            <Typography className={classes.instructions}>{getStepContent(activeStep)}</Typography>
            <div>





            <div className={classes.root}>
      <AppBar position="static" color="default">
        <Tabs
          value={value}
          onChange={handleChange}
          indicatorColor="primary"
          textColor="primary"
          variant="scrollable"
          scrollButtons="auto"
          aria-label="scrollable auto tabs example"
        >
          <Tab label="ASSURANCE" {...a11yProps(0)} />
          <Tab label="POLICIER" {...a11yProps(1)} />
          <Tab label="EXPERT" {...a11yProps(2)} />
          <Tab label="POMPIER" {...a11yProps(3)} />
        </Tabs>
      </AppBar>
      <TabPanel value={value} index={0}>
        Assurance
      </TabPanel>
      <TabPanel value={value} index={1}>
        Procedure Policier
      </TabPanel>
      <TabPanel value={value} index={2}>
        Demarche de l'expert
      </TabPanel>
      <TabPanel value={value} index={3}>
        Constast du Pompier
      </TabPanel>
    </div>

    

              <Grid
			  container
			  direction="row"
			  justify="center"
			  alignItems="center"
			>

              <Button disabled={activeStep === 0} onClick={handleBack} className={classes.button}>
                Back
              </Button>
              <Button
                variant="contained"
                color="primary"
                onClick={handleNext}
                className={classes.button}
              >
                {activeStep === steps.length - 1 ? 'Finish' : 'Next'}
              </Button>

              </Grid>
            </div>
          </div>
        )}
      </div>
    </div>
     





       </Paper>
    </Container>
  );
}
